import os
import structlog
from github import Github, Auth
from typing import List, Dict

logger = structlog.get_logger("swarm_agent")

class SwarmEngineer:
    """The Autonomous PR Agent - Capable of fixing repository issues and proposing PRs."""
    
    def __init__(self, token: str = None):
        self.token = token or os.getenv("GITHUB_TOKEN")
        if not self.token:
            logger.warning("no_github_token_found", action="skipping_remote_ops")
            self.github = None
        else:
            auth = Auth.Token(self.token)
            self.github = Github(auth=auth)

    def propose_fix(self, repo_name: str, branch_name: str, file_path: str, new_content: str, commit_msg: str):
        """Creates a new branch, pushes a fix, and opens a Pull Request."""
        if not self.github:
            return "Failed: No GitHub Authentication."

        try:
            repo = self.github.get_repo(repo_name)
            base_branch = repo.get_branch("main")
            
            # 1. Create a new branch
            try:
                repo.create_git_ref(ref=f"refs/heads/{branch_name}", sha=base_branch.commit.sha)
                logger.info("branch_created", branch=branch_name)
            except Exception as e:
                logger.warning("branch_exists_or_error", error=str(e))

            # 2. Update the file
            contents = repo.get_contents(file_path, ref=branch_name)
            repo.update_file(
                contents.path,
                commit_msg,
                new_content,
                contents.sha,
                branch=branch_name
            )
            logger.info("file_updated", path=file_path)

            # 3. Create Pull Request
            pr = repo.create_pull(
                title=f"Autonomous Fix: {commit_msg}",
                body=f"This PR was automatically generated by DishaOS Swarm Agent.\n\nTarget File: {file_path}",
                head=branch_name,
                base="main"
            )
            logger.info("pull_request_created", pr_url=pr.html_url)
            return pr.html_url

        except Exception as e:
            logger.error("swarm_operation_failed", error=str(e))
            return f"Error: {str(e)}"

if __name__ == "__main__":
    # Example usage (Mock)
    engineer = SwarmEngineer()
